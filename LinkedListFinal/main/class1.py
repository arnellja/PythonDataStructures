class Course(object):
    """description of class"""


    def __init__(self, number = 0, name = "Empty", credit_hour = 0.00, grade = 0.00, next = None):
        self.number = number
        self.name = name
        self.credit_hour = credit_hour
        self.grade = grade
        self.next = next


    def number():
        return self.number

    def name():
        return self.name

    def credit_hour():
        return self.credit_hour

    def grade():
        return self.grade

    def next():
        return self.next

    def __str__():
        return number, ", ", name, ", ", credit_hour, ", ", grade